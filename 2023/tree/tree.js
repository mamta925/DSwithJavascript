/**
 * A data structure that consists of nodes in a parent /child relationship
 * 
 * Binary Trees: 
 *             Each node can have at most 2 child
 *  Binary Search Trees(BST): 
 *             Each node can have at most 2 child
 *             Every node to the the left of a parent node always less than the parent and vice versa
 * 
 * Tree Traversal:
 *             Breadth First Search
 *             Depth first Search      
 * 
 * Heaps: 
 *      A Heap is a special Tree-based data structure in which the tree is a complete binary tree. 
 *      Generally, Heaps can be of two types:        
 * Max-Heap: 
 *        In a Max-Heap the key present at the root node must be greatest among 
 *        the keys present at all of it’s children. The same property must be recursively 
 *         true for all sub-trees in that Binary Tree.
 *  Min-Heap: 
 *         In a Min-Heap the key present at the root node must be minimum among the keys present 
 *         at all of it’s children. The same property must be recursively true for all sub-trees in
 *         that Binary Tree.
 */